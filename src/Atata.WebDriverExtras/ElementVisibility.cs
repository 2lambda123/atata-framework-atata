namespace Atata
{
    /// <summary>
    /// Specifies the visibility of the element.
    /// </summary>
    public enum ElementVisibility
    {
        /// <summary>
        /// Finds only the visible elements.
        /// </summary>
        Visible,

        /// <summary>
        /// Finds only the invisible elements.
        /// </summary>
        Invisible,

        /// <summary>
        /// Finds the elements with any visibility.
        /// </summary>
        Any
    }
}
