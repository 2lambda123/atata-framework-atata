namespace Atata
{
    /// <summary>
    /// Specifies the visibility of element.
    /// </summary>
    public enum ElementVisibility
    {
        /// <summary>
        /// Finds only visible elements.
        /// </summary>
        Visible,

        /// <summary>
        /// Finds only invisible elements.
        /// </summary>
        Invisible,

        /// <summary>
        /// Finds elements with any visibility.
        /// </summary>
        Any
    }
}
